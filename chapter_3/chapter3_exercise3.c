#include <stdio.h>

int main() {
    printf("%d\n", 120);
    printf(" %d\n", 120);

    printf("%d-%d-%d\n", 120, 120, 120);
    printf("%d -%d -%d\n", 120, 120, 120);

    printf("%f\n", 120.1);
    printf("%f \n", 120.1);

    printf("%f,%f\n", 120.1, 120.1);
    printf("%f, %f\n", 120.1, 120.1);

    return 0;
}
